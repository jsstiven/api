package com.example.demo.views;

public class VistaUsuarios {

    private Integer cc;
    private String nombres;
    private String apellidos;
    private String usuario;
    private String correo;
    private String contrasena;
    private Integer programa_id;
    private Integer roles_idroles;

    public Integer getCc() {
        return cc;
    }

    public void setCc(Integer cc) {
        this.cc = cc;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getContrasena() {
        return contrasena;
    }

    public void setContrasena(String contrasena) {
        this.contrasena = contrasena;
    }

    public Integer getPrograma_id() {
        return programa_id;
    }

    public void setPrograma_id(Integer programa_id) {
        this.programa_id = programa_id;
    }

    public Integer getRoles_idroles() {
        return roles_idroles;
    }

    public void setRoles_idroles(Integer roles_idroles) {
        this.roles_idroles = roles_idroles;
    }

}
